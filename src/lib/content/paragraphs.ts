// Get all paragraphs combined
export function getAllParagraphs(): string[][] {
  return [
    ...DAILY_LIFE_PARAGRAPHS,
    ...NATURE_PARAGRAPHS,
    ...TECHNOLOGY_PARAGRAPHS,
    ...EDUCATION_PARAGRAPHS,
    ...CAREER_PARAGRAPHS,
    ...CREATIVE_PARAGRAPHS,
    ...HEALTH_PARAGRAPHS,
    ...TRAVEL_PARAGRAPHS,
    ...STATISTICS_PARAGRAPHS,
    ...EVENTS_PARAGRAPHS,
    ...getAllExpandedParagraphs(),
  ];
}

// Get paragraphs by category
export function getParagraphsByCategory(
  category:
    | "daily_life"
    | "nature"
    | "technology"
    | "education"
    | "career"
    | "creative"
    | "health"
    | "travel"
): string[][] {
  switch (category) {
    case "daily_life":
      return DAILY_LIFE_PARAGRAPHS;
    case "nature":
      return NATURE_PARAGRAPHS;
    case "technology":
      return TECHNOLOGY_PARAGRAPHS;
    case "education":
      return EDUCATION_PARAGRAPHS;
    case "career":
      return CAREER_PARAGRAPHS;
    case "creative":
      return CREATIVE_PARAGRAPHS;
    case "health":
      return HEALTH_PARAGRAPHS;
    case "travel":
      return TRAVEL_PARAGRAPHS;
  }
}

// Get a random paragraph
export function getRandomParagraph(): string[] {
  const all = getAllParagraphs();
  return all[Math.floor(Math.random() * all.length)];
}
